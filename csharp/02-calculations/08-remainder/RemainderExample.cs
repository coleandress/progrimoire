using System;

namespace ProgrimoireCSharpExamples
{
    internal static class RemainderExample
    {
        public static void Main(string[] args)
        {
            var ten = 10;

            Console.WriteLine($"Remainder when {ten} is divided by 1 is: {ten % 1}");
            Console.WriteLine($"Remainder when {ten} is divided by 2 is: {ten % 2}");
            Console.WriteLine($"Remainder when {ten} is divided by 3 is: {ten % 3}");
            Console.WriteLine($"Remainder when {ten} is divided by 4 is: {ten % 4}");
            Console.WriteLine($"Remainder when {ten} is divided by 5 is: {ten % 5}");
            Console.WriteLine($"Remainder when {ten} is divided by 6 is: {ten % 6}");
            Console.WriteLine($"Remainder when {ten} is divided by 7 is: {ten % 7}");
            Console.WriteLine($"Remainder when {ten} is divided by 8 is: {ten % 8}");
            Console.WriteLine($"Remainder when {ten} is divided by 9 is: {ten % 9}");
            Console.WriteLine($"Remainder when {ten} is divided by 10 is: {ten % 10}");
        }
    }
}